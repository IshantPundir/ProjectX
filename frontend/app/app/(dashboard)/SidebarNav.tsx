/* ─── Nav items ─── */

const NAV_ITEMS = [
  { href: "/", label: "Dashboard", icon: IconHome },
  { href: "/settings/team", label: "Team", icon: IconUsers },
  { href: "/settings/org-units", label: "Org Units", icon: IconBuilding },
  { href: "/profile", label: "Profile", icon: IconUserCircle },
] as const;

/* ─── Component ─── */

interface SidebarNavProps {
  userEmail: string;
}

export default function SidebarNav({ userEmail }: SidebarNavProps) {
  const pathname = usePathname();
  const router = useRouter();
  const [signingOut, setSigningOut] = useState(false);

  async function handleSignOut() {
    setSigningOut(true);
    const supabase = createClient();
    await supabase.auth.signOut();
    router.push("/login");
    router.refresh();
  }

  function isActive(href: string) {
    if (href === "/") return pathname === "/";
    return pathname.startsWith(href);
  }

  return (
    <div className="flex flex-col h-full">
      {/* Nav links */}
      <nav className="flex-1 space-y-1" aria-label="Main navigation">
        {NAV_ITEMS.map(({ href, label, icon: Icon }) => {
          const active = isActive(href);
          return (
            <Link
              key={href}
              href={href}
              className={`flex items-center gap-2.5 px-3 py-2 rounded-lg text-sm font-medium transition-colors duration-150 ${
                active
                  ? "bg-green-50 text-green-600"
                  : "text-zinc-600 hover:text-zinc-900 hover:bg-zinc-50"
              }`}
              aria-current={active ? "page" : undefined}
            >
              <Icon className="w-4 h-4 shrink-0" />
              {label}
            </Link>
          );
        })}
      </nav>

      {/* Divider + user info + sign out */}
      <div className="border-t border-zinc-200 pt-4 mt-4">
        <div className="px-3 mb-3">
          <p className="text-xs text-zinc-400 truncate" title={userEmail}>
            {userEmail}
          </p>
        </div>
        <button
          onClick={handleSignOut}
          disabled={signingOut}
          className="flex items-center gap-2 w-full px-3 py-2 rounded-lg text-sm text-zinc-500 hover:text-red-600 hover:bg-red-50 transition-colors duration-150 cursor-pointer disabled:opacity-50"
          aria-label="Sign out"
        >
          <IconLogout className="w-4 h-4 shrink-0" />
          {signingOut ? "Signing out…" : "Sign out"}
        </button>
      </div>
    </div>
  );
}
